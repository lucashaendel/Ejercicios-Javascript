let str = prompt("ingrese: casa, perro, pelota, arbol, o genio")
switch(str){
  case 'casa': console.log("home")
  break
  case 'perro': console.log("dog")
  break
  case 'pelota': console.log("ball")
  break
  case 'arbol': console.log("tree")
  break
  case 'genio': console.log("genius")
  break
  default: console.log("la palabra no es correcta")

}